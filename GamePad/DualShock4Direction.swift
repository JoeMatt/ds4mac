import Foundation



enum DualShock4Direction:UInt8 {

	case up        = 0x00
	case rightUp   = 0x01
	case right     = 0x02
	case rightDown = 0x03
	case down      = 0x04
	case leftDown  = 0x05
	case left      = 0x06
	case leftUp    = 0x07

}
