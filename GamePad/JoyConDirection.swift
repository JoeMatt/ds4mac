import Foundation



enum JoyConDirection:UInt8 {

	case right     = 0x00
	case rightDown = 0x01
	case down      = 0x02
	case leftDown  = 0x03
	case left      = 0x04
	case leftUp    = 0x05
	case up        = 0x06
	case rightUp   = 0x07

}
